        <section class="faq-item">
            <div class="pergunta"><h2>Qual é o prazo legal para o pagamento das verbas rescisórias?</h2></div>
            <div class="resposta">
                <p>Existem dois prazos principais:</p>
                <ul>
                    <li>Aviso prévio trabalhado: o pagamento deve ser feito no primeiro dia útil seguinte ao término do contrato.</li>
                    <li>Aviso prévio indenizado ou dispensa imediata: o pagamento deve ocorrer em até 10 dias corridos a partir da rescisão.</li>
                </ul>
            </div>
        </section>

        <section class="faq-item">
            <div class="pergunta"><h2>Como é feita a contagem desse prazo de 10 dias?</h2></div>
            <div class="resposta">
                <p>A contagem segue as seguintes regras:</p>
                <ul>
                    <li>Exclui-se o dia da notificação da dispensa ou do pedido de demissão</li>
                    <li>Inclui-se o dia do vencimento no cálculo</li>
            </ul>
            </div>
        </section>

        <section class="faq-item">
            <div class="pergunta"><h2>Quais são as consequências do não pagamento no prazo?</h2></div>
            <div class="resposta">
                <h3>Multa por Atraso</h3>
                <p>O empregador que não efetuar o pagamento das verbas rescisórias dentro do prazo legal está sujeito a uma penalidade estabelecida no artigo 477, § 8º da CLT. A sanção consiste no pagamento de uma multa correspondente a um salário mensal do empregado.</p>
                
                <h3>Atualização Monetária e Juros</h3>
                <p>Além da multa, os valores devidos sofrerão atualização monetária e acréscimo de juros. Isso significa que a quantia será reajustada conforme índices oficiais e sofrerá incidência de juros, aumentando o montante final a ser quitado pelo empregador.</p>
                
                <h3>Processo Trabalhista</h3>
                <p>O trabalhador pode acionar a Justiça do Trabalho para garantir seus direitos. Caso uma ação judicial seja movida, o empregador poderá ter que arcar com despesas adicionais, como honorários advocatícios, taxas judiciais e outros custos legais. Dependendo do caso, pode haver ainda indenização por danos morais, caso seja comprovado que o atraso causou prejuízos significativos ao empregado.</p>
            </div>
        </section>

        <section class="faq-item">
            <div class="pergunta"><h2>É possível negociar o aviso prévio?<h2></div>
            <div class="resposta">
                <p>Sim, existe a possibilidade de negociação do aviso prévio entre as partes. As opções incluem:</p>
                <ul>
                    <li>Cumprimento normal do período</li>
                    <li>Dispensa do cumprimento com pagamento</li>
                    <li>Redução da jornada durante o aviso</li>
                </ul>
                <p><strong>Importante:</strong>Qualquer acordo deve ser formalizado por escrito.</p>
            </div>
        </section>

        <section class="faq-item">
            <div class="pergunta"><h2>Qual a diferença entre aviso prévio trabalhado e indenizado?</h2></div>
            <div class="resposta">
                    <h3>Aviso Prévio Trabalhado:</h3>
                    <ul>
                        <li>O colaborador continua trabalhando durante o período</li>
                        <li>Duração de 30 dias + 3 dias por ano de serviço, limitado a 90 dias.</li>
                        <li>Mantém todas as obrigações e direitos normais</li>
                    </ul>
                    
                    <h3>Aviso Prévio Indenizado:</h3>
                    <ul>
                        <li>Dispensa imediata do trabalho</li>
                        <li>Pagamento do período sem necessidade de comparecimento</li>
                        <li>Mesmo cálculo de duração do trabalhado</li>
                    <ul>
            </div>
        </section>

        <section class="faq-item">
            <div class="pergunta"><h2>Como calcular férias proporcionais para menos de um ano de trabalho?</h2></div>
            <div class="resposta">
                <div class="calculo">
                    <h3>Exemplo com salário de R$ 1.800 e 10 meses trabalhados:</h3>
                    <ol>
                        <li>1. Férias proporcionais = (R$ 1.800 ÷ 12) × 10 meses = R$ 1.500</li>
                        <li>2. Adicional de férias (1/3) = R$ 1.500 ÷ 3 = R$ 500</li>
                        <li>3. Total = R$ 1.500 + R$ 500 = R$ 2.000</li>
                    </ol>
                </div>
            </div>
        </section>

        <section class="faq-item">
            <div class="pergunta"><h2>O seguro-desemprego é garantido em todas as modalidades de rescisão?</h2></div>
            <div class="resposta">
                <p>Não. O direito ao seguro-desemprego varia conforme o tipo de rescisão:</p>

                <ul>
                    <li>Demissão sem justa causa: Elegível (cumprindo requisitos)</li>
                    <li>Demissão por justa causa: Não elegível</li>
                    <li>Rescisão consensual: Não elegível, mas pode movimentar até 80% do saldo do FGTS</li>
                <ul>
            
            </div>
        </section>

        <section class="faq-item">
            <div class="pergunta"><h2>O que fazer em caso de divergências nas verbas rescisórias?</h2></div>
            <div class="resposta">
                <p>Em caso de discordância, siga estes passos:<p>

                <ol>
                    <li>Tente uma negociação direta com a empresa</li>
                    <li>Guarde todos os documentos e comprovantes</li>
                    <li>Busque orientação sindical se necessário</li>
                    <li>Procure a Justiça do Trabalho</li>
                </ol>    
            
            </div>
